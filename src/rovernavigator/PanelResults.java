    public void updateStatus() {
        DecimalFormat formatDistance = new DecimalFormat("0.000");
        DecimalFormat formatDegrees = new DecimalFormat("0.0");
        DecimalFormat formatInteger = new DecimalFormat("0");
        
        labelDistance.setText(formatDistance.format(main.motionPath.getTravelDistance()));
        labelDegrees.setText(formatDegrees.format(main.motionPath.getTravelDegrees()));
        labelExperimentError.setText(formatDistance.format(main.motionPath.getExperimentError()));
        
        labelAvgError.setText(formatDistance.format(main.motionPath.getExperimentAvgError()));
        labelPathScore.setText("Path Score = " + formatDistance.format(main.motionPath.getPathScore()));

        labelCommandCount.setText(formatInteger.format(main.commands.getCount()));
        
        if (main.motionPath.hazardIntersects > 0) {
            labelHazardCount.setForeground(Color.red);
            labelHazardCount.setText(formatInteger.format(main.motionPath.hazardIntersects));
        } else {
            labelHazardCount.setForeground(new Color(0x00,0xb0,0x00));
            labelHazardCount.setText("None");
        }
        
        if (main.motionPath.exitMapArea > 0) {
            labelExitMap.setForeground(Color.red);
            labelExitMap.setText("YES (" + formatInteger.format(main.motionPath.exitMapArea) + ")");
        } else {
            labelExitMap.setForeground(new Color(0x00,0xb0,0x00));
            labelExitMap.setText("No");
        }      
        
        int tier = main.motionPath.getTier();
        if (tier == 1) {
            labelTier.setForeground(new Color(0x00,0xb0,0x00));
        } else {
            labelTier.setForeground(Color.red);
        }
        labelTier.setText(formatInteger.format(tier));
        
        String msg = "";
        msg += "<html>";
        msg += "<table>";
        
        msg += "<tr>";
        msg += "<td>";
        msg += "ID";
        msg += "</td>";
        msg += "<td>";
        msg += "Completed";
        msg += "</td>";
        msg += "<td>";
        msg += "Error (m)";
        msg += "</td>";
        msg += "</tr>";
        
        int e = 0;
        while (e < main.motionPath.experiments) {
            e++;
            msg += "<tr>";
            msg += "<td>";
            msg += main.motionPath.testResult[e].id;
            msg += "</td>";
            msg += "<td>";
            if (main.motionPath.testResult[e].completed) msg += "Yes";
            else msg += "No";
            msg += "</td>";
            msg += "<td>";
            msg += formatDistance.format(main.motionPath.testResult[e].distError);
            msg += "</td>";
            msg += "</tr>";
        }
        if (e == 0) {
            msg += "<tr>";
            msg += "<td>";
            msg += "No";
            msg += "</td>";
            msg += "<td>";
            msg += "Experiments";
            msg += "</td>";
            msg += "<td>";
            msg += "Found";
            msg += "</td>";
            msg += "</tr>";            
        }

        
        msg += "</table>";
            msg += "<br>";
            msg += formatDistance.format(main.map.radius);
            msg += " Experiments";
            msg += " Radius";
        msg += "</html>";
        labelTestResults.setText(msg);
    }
    
    
